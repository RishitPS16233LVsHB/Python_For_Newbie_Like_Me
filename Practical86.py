set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

# Intersection
print("Intersection:", set1.intersection(set2))

# Union
print("Union:", set1.union(set2))

# Set Difference
print("Set Difference:", set1.difference(set2))

# Symmetric Difference
print("Symmetric Difference:", set1.symmetric_difference(set2))
